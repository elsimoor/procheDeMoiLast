/**
 * Resolve a translation key for a given locale.  If the key is not
 * present in the dictionary, the key itself will be returned as a
 * fallback.  Components can import this function directly or use
 * wrapper hooks to simplify usage with contexts.
 *
 * @param key The translation key
 * @param locale The locale to translate into
 */
export function t(key: string, locale: Locale): string {
  const value = translations[locale]?.[key]
  if (value === undefined) return key
  return value
}